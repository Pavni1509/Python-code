def add(a, b):
    """
    Returns the sum of a and b.
    
    Parameters:
    a (int, float): First number
    b (int, float): Second number
    
    Returns:
    int, float: Sum of a and b
    """
    return a + b

def subtract(a, b):
    """
    Returns the difference of a and b.
    
    Parameters:
    a (int, float): First number
    b (int, float): Second number
    
    Returns:
    int, float: Difference of a and b
    """
    return a - b

