#Python code to demonstrate assignment operators
#Intialising the variable
a=10
print(f"a={a}")
#Using += to add and assign
a +=15
print(f"a+=15->{a}")
#Using -= to subtract and assign
a -=5
print(f"a-=15->{a}")
#Using *= to subtract and assign
a *=15
print(f"a*=15->{a}")
#Using /= to subtract and assign
a /=15
print(f"a/=15->{a}")
#Using %= to find remainder and assign
a %=5
print(f"a%=15->{a}")
#Using //= to floor-devide and assign
a//=5
print(f"a//=15->{a}")
#Using **= for exponentiation and assign
a**=5
print(f"a**=5->{a}")
